namespace SorterApp
{
    /// <summary>
    /// The file format type extension. Set the format of file
    /// with specific format.
    /// </summary>
    public enum FileFormatTypeExtension
    {
        /// <summary>
        /// There's not file format specified.
        /// </summary>
        None = 0,

        /// <summary>
        /// Sets the file format to .txt.
        /// </summary>
        Text = 1,

        /// <summary>
        /// Sets the file format to .doc.
        /// </summary>
        Document = 2,

        /// <summary>
        /// Sets the file format to .pdf.
        /// </summary>
        Pdf = 3,

    }
}
